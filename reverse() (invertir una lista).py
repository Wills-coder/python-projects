#Método reverse(), para invertir una  lista y sus elementos.

vocales = ["a","e","i","o","u"]
print(f"Lista: {vocales}")

vocales.reverse()
print(f"\nLista: {vocales}")
print(f"\nLista: {vocales}")
print(f"posición 0: {vocales[0]}")

vocales.reverse()
print(f"\nLista: {vocales}")
print(f"posición 0: {vocales[0]}")
